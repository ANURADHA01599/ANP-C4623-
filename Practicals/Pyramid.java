package corejava;
//create a clss
public class Pyramid 
{
    //main method
	public  void main(String[] args) 
	{
		//take a three variable
		int i,j,k ;
		for(i=1;i<=5;i++);
		{
			for(j=5;j>i;j--);
			{
			   System.out.println(" ");	
			}
            for(k=1;k<=i;k++);
            {
               System.out.println("* ");
            }
            {
            System.out.println("\n ");
         	}
		}
	}
}